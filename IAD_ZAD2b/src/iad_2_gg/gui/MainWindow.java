    private void showTestResult(double[] result) {
        jTextTestResult.setText("");
        DecimalFormat df = new DecimalFormat("0.####");
        for (double d : result) {
            jTextTestResult.append(df.format(d) + "\n");
        }
    }

    private void showErrorAfterTraining() {
        DecimalFormat df = new DecimalFormat("0.########");
        List<Double> errorLog = networkHelper.getTrainingErrorLog();
        jTextError.setText(df.format(errorLog.get(errorLog.size() - 1)));
    }

