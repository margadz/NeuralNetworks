    private List<Integer> getExcludedList() {
        List<Integer> excluded = new ArrayList<>();
        String input = jTextExcluded.getText();
        if (!input.isEmpty()) {
            String[] split = input.split(",");
            for (String s : split) {
                excluded.add(Integer.parseInt(s));
            }
        }
        return excluded;
    }

