    private void jButtonCreateFactory4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreateFactory4ActionPerformed
        jTextConfusionMatrix.setText("");
        try {
            networkHelper.starTraining(
                    Integer.parseInt(jTextEpochNumber.getText()),
                    Double.parseDouble((String) jComboLearningRate.getSelectedItem()),
                    Double.parseDouble((String) jComboMomentum.getSelectedItem()),
                    jCheckRandomData.isSelected(),
                    jComboSubData.getSelectedIndex(),
                    getExcludedList()
            );
        } catch (Network.NoNetworkException ex) {
            JOptionPane.showMessageDialog(this, "Sieć nie zostałą utworzona", "Błąd sieci", JOptionPane.ERROR_MESSAGE);
            return;
        } catch (Exception ex) {
            Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
            return;
        }
        showErrorAfterTraining();
        if (jComboSubData.getSelectedIndex() == 0) {
            ChartDialog chart = new ChartDialog(this, false, "Błąd zbioru treningowego ", "epoka", "błąd", networkHelper.getTrainingErrorLog(), "błąd", 50);
            chart.setVisible(true);
        } else {
            showConfusionMatrix(networkHelper.getConfusionMatrix());
            ChartDialog chart1 = new ChartDialog(this, false, "Błąd zbioru walidacyjnego", "epoka", "błąd",
                    networkHelper.getTrainingErrorLog(), "zbiór treningowy",
                    networkHelper.getValidationErrorLog(), "zbiór walidacyjny", 50);
            ChartDialog chart2 = new ChartDialog(this, false, "Odsetek poprawnie zaklasyfikowanych przykładów", "epoka", "%", networkHelper.getTestScoreLog(), "klasyfikacje", 50);
            chart2.setVisible(true);
            chart1.setVisible(true);
        }
    }//GEN-LAST:event_jButtonCreateFactory4ActionPerformed

