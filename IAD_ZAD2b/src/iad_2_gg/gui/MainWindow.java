    public void showConfusionMatrix(List<List<Integer>> matrix){
        StringBuilder sb = new StringBuilder("");
        sb.append("\t");
        int it = 0;
        for (Integer i : matrix.get(0)){
            sb.append(++it).append("\t");
        }
        sb.append("\n");
        for (int i = 0; i < matrix.size(); i++){
            sb.append(i + 1).append("\t");
            for (Integer j : matrix.get(i)){
                sb.append(j).append("\t");
            }
            sb.append("\n");
        }
        jTextConfusionMatrix.setText(sb.toString());
    }
