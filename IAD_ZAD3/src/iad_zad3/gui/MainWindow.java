    private void jButtonLoadFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoadFileActionPerformed
        try {
            helper.openFile(this);
            switchChoiceButtons(true);
        } catch (NoFileException ex) {
            switchChoiceButtons(false);
            switchRunButtons(false);
            Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            switchChoiceButtons(false);
            switchRunButtons(false);
            Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            switchChoiceButtons(false);
            switchRunButtons(false);
            Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonLoadFileActionPerformed

    private void jButtonShowChartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonShowChartActionPerformed
        if (chartWorker != null) {
            chartWorker.cancel(true);
        }
        clusteringAlgorithm.setUpNewCentroids(Integer.parseInt(jTextCentroids.getText()));
        showKMeansChart();
        jTextError.setText(String.valueOf(clusteringAlgorithm.getError()));
    }//GEN-LAST:event_jButtonShowChartActionPerformed

    private void jButtonNextIterationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNextIterationActionPerformed
        clusteringAlgorithm.moveCentroids();
        showKMeansChart();
        jTextError.setText(ChartWorker.df.format(clusteringAlgorithm.getError()));
    }//GEN-LAST:event_jButtonNextIterationActionPerformed

    private void jButtonPlayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPlayActionPerformed
        if (chartWorker != null) {
            chartWorker.cancel(true);
        }
        chartWorker = new ChartWorker(jPanelChart, jPanelVoronoi, jTextError, jProgressBar1, clusteringAlgorithm,
                jCheckAnimate.isSelected(), Integer.parseInt(jTextFrameDuration.getText()));
        chartWorker.execute();
    }//GEN-LAST:event_jButtonPlayActionPerformed

    private void jButtonStopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonStopActionPerformed
        if (chartWorker != null) {
            chartWorker.cancel(true);
        }
    }//GEN-LAST:event_jButtonStopActionPerformed

    private void jButtonChooseKmeansActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonChooseKmeansActionPerformed
        try {
            clusteringAlgorithm = new KmeansClustering(new EuclidianDistance(), helper.getData(), Integer.parseInt(jTextCentroids.getText()),
                    Integer.parseInt(jTextIterations.getText()));
            jProgressBar1.setMaximum(Integer.parseInt(jTextIterations.getText()));
            jLabelAlgorithm.setText("k-średnich");
            switchRunButtons(true);
        } catch (NoFileException ex) {
            switchChoiceButtons(false);
            switchRunButtons(false);
        }
    }//GEN-LAST:event_jButtonChooseKmeansActionPerformed

    private void jButtonChooseNeuralGasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonChooseNeuralGasActionPerformed
        try {
            gatherParameters(evt.getActionCommand());
            clusteringAlgorithm = new NeuralGasClustering(new EuclidianDistance(),
                    helper.getData(),
                    Integer.parseInt(jTextCentroids.getText()),
                    Integer.parseInt(jTextIterations.getText()),
                    helper.getSOMParameters()
            );
            jProgressBar1.setMaximum(Integer.parseInt(jTextIterations.getText()));
            jLabelAlgorithm.setText("gazu neuronowego");
            switchRunButtons(true);
        } catch (NoFileException ex) {
            switchChoiceButtons(false);
            switchRunButtons(false);
        } catch (GUIHelper.NoParamsException ex) {
            switchRunButtons(false);
            Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonChooseNeuralGasActionPerformed

