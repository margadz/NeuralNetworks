    private void showKMeansChart() {
        jPanelChart.removeAll();
        ChartPanel chartPanel = new ChartPanel(Charts.getKmeansChart(clusteringAlgorithm.getData(), clusteringAlgorithm.getCentroids()));
        chartPanel.setPreferredSize(new Dimension(600, 600));
        chartPanel.setDomainZoomable(true);
        jPanelChart.setLayout(new java.awt.BorderLayout());
        jPanelChart.add(chartPanel, BorderLayout.CENTER);
        jPanelChart.validate();
    }

    private void switchChoiceButtons(boolean b) {
        jButtonChooseKmeans.setEnabled(b);
        jButtonChooseKohonen.setEnabled(b);
        jButtonChooseNeuralGas.setEnabled(b);
    }

    private void switchRunButtons(boolean b) {
        jButtonShowChart.setEnabled(b);
        jButtonNextIteration.setEnabled(b);
        jButtonPlay.setEnabled(b);
        jButtonStop.setEnabled(b);
        jButtonChangeData.setEnabled(b);
    }

    private void gatherParameters(String algorithm) {
        switch (algorithm) {
            case "kohonen":
                helper.setSOMParameters(Double.parseDouble(jTextKohLambdaMax.getText()),
                        Double.parseDouble(jTextKohLambdaMin.getText()),
                        Double.parseDouble(jTextKohEtaMax.getText()),
                        Double.parseDouble(jTextKohEtaMin.getText()));
                break;
            case "neuralgas":
                helper.setSOMParameters(Double.parseDouble(jTextNeuLambdaMax.getText()),
                        Double.parseDouble(jTextNeuLambdaMin.getText()),
                        Double.parseDouble(jTextNeuEtaMax.getText()),
                        Double.parseDouble(jTextNeuEtaMin.getText()));
                break;
        }
    }

